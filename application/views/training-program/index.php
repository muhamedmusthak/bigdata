			<p class="description">El curso de análisis de datos no estructurado es un curso práctico que enseña al estudiante 
a obtener experiencia en el manejo de datos de texto. En el curso se enseñarán técnicas 
de representación y algoritmos de preprocesamiento de texto. El estudiante será capaz 
de construir aplicaciones de búsqueda de texto y minería de texto como la atribución de 
