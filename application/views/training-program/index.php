conceptos de probabilidad, estadística, y álgebra lineal en el contexto de problemas de 
negocios y Data Science. Los estudiantes adquirirán experiencia en el entendimiento y la 
construcción de modelos de Machine Learning para predicción, clasificación, clustering, 
y reducción de dimensionalidad. A través del curso los estudiantes se familiarizarán con 
