			<p class="descriptiontitle">Descripción del Curso</p>
			<p class="description">El curso de Big Data Specialist es la continuación del curso BDIPBD, y provee un estudio 
detallado  del  ecosistema  de  Hadoop.  El  curso  de  Specialist  examina  en  detalle  las 
capas de recolección, organización, procesamiento, consulta y analítica del ecosistema 
de Big Data. Los estudiantes serán capaces de recolectar, almacenar y procesar datos 
estructurados, semi estructurados, no estructurados y transmitidos en tiempo real. Este 
curso también se enfoca en los procesos de organización de datos semi estructurados 
y basados en grafos usando bases de datos No-SQL. Los estudiantes serán capaces de 
construir herramientas de búsqueda y de realizar análisis de grafos al finalizar este curso</p>
			
			<a href="<?php echo base_url('BigdatasiteController/specialist/3');?>" class="btnWhite" style="background-color:#435ca9;color:white;margin-bottom: 4%;">Specialist Data Ingestion </a>
			<p class="descriptiontitle">Descripción del Curso</p>
			<p class="description">La recolección y captura de datos es un proceso crucial en los proyectos de Big Data. 
Ya sea que se estén capturando los cambios en los datos estructurados hacia data lakes 
o  usando  herramientas  open  source  para  la  recolección  de  datos  no  estructurados  o 
transmitidos en tiempo real, la experiencia con la recolección de datos es sumamente 
necesaria.  El  curso  de  BDISDI  prepara  a  los  estudiantes  para  convertirse  en  expertos 
en todos los tipos de recolección de datos. La experiencia práctica es proporcionada a 
través del uso de herramientas especializadas en la recolección y captura de datos en 
Hadoop</p>
			
			<a href="<?php echo base_url('BigdatasiteController/specialist/3');?>" class="btnWhite" style="background-color:#435ca9;color:white;margin-bottom: 4%;">Specialist Data Management </a>
			<p class="descriptiontitle">Descripción del Curso</p>
			<p class="description">Los estudiantes de este curso (BDISDM) obtendrán habilidades prácticas en el manejo 
y almacenamiento de datos de soluciones de analítica sobre Big Data, lo cual incluye 
administración de sistemas relacionales de bases de datos (RDBMS - bases de datos 
operacionales y data warehouses), un análisis detallado de bases de datos No-SQL 
(bases de datos de grafos, bases de datos documentales, bases de datos columnares 
y bases de datos Key-Value) y almacenamiento distribuido en Hadoop. Los estudiantes 
serán  capaces  de  gestionar  y  organizar  datos  estructurados,  semiestructurados  y  no 
estructurados  en  Hadoop  para  soluciones  de  análisis  tanto  en  batch  como  de  forma 
interactiva.</p>
			
