en texto. El estudiante adquirirá experiencia en técnicas de procesamiento de lenguaje 
natural y aplicaciones como correferenciación, reconocimiento de entidades nombradas, 
y POS tagging.  </p>
			
			<a href="<?php echo base_url('BigdatasiteController/advanced/4');?>" class="btnWhite" style="background-color:#435ca9;color:white;margin-bottom: 4%;">Specialized Course: R- Development</a>
			<p class="descriptiontitle">Descripción del Curso</p>
			<p class="description">R  es  un  toolkit  estadístico  y  de  Machine  Learning  ampliamente  usado  que  sigue  el 
paradigma  de  programación  funcional.  La  experiencia  en  R  es  crucial  para  realizar  el 
análisis  de  datos  exploratorio,  construir  modelos  de  Machine  Learning,  y  desarrollar 
pipelines de Analytics. En este curso, los estudiantes tendrán experiencia práctica para 
entender los componentes y las bases de R, la preparación y transformación de datos, 
así como la construcción de pipelines en R</p>
			
			
			<a href="<?php echo base_url('BigdatasiteController/advanced/4');?>" class="btnWhite" style="background-color:#435ca9;color:white;margin-bottom: 4%;">Specialized Course: Data Visualization and Business Storytelling </a>
			<p class="descriptiontitle">Descripción del Curso</p>
			<p class="description">El objetivo de Data Science es generar valor a partir de los datos. Para encontrar 
dicho valor y comunicarlo de manera efectiva, una de las actividades más importantes es 
la visualización. Por medio de la visualización en etapas iniciales de un proyecto de Data 
Science, los científicos de datos adquieren un mayor entendimiento de sus datos y el 
problema, permitiéndoles generar hipótesis más certeras. A su vez, en etapas avanzadas 
la visualización ayuda a comunicar efectivamente los resultados de su análisis.
Este  curso  introduce  al  estudiante  al  manejo  y  transformación  de  los  datos  para 
visualizarlos usando R. A su vez, el curso introduce los conceptos de diseño de gráficos y 
narrativas con datos. De esta manera el estudiante adopta una filosofía para representar 
y comunicar la evidencia de forma eficiente a cualquier audiencia</p>
