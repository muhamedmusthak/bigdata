			<p class="description">El  propósito  de  este  curso  es  proporcionar  experiencia  en  los  frameworks  de 
procesamiento de datos de Hadoop. El curso realiza una introducción al ecosistema de 
Hadoop y discute varios de los problemas que pueden ser resueltos por el mismo en 
los cuales se incluyen temas como: procesamiento por lotes y de datos en tiempo real. 
Los estudiantes de este curso serán capaces de manipular datos y construir flujos para el 
procesamiento de datos en Hadoop</p>
