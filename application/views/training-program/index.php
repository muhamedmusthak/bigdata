contacto  con  múltiples  técnicas  de  serialización  y  con  herramientas  para  el  desarrollo 
de soluciones, alineadas a las prácticas de la industria. Los estudiantes serán capaces al 
finalizar este curso de: administrar operaciones ELT sobre Hadoop, de generar reportes 
